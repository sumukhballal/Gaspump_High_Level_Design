package Datastore;

public class Datastore1 extends Datastore {

   private int temp_c;
   private int cash;
   private int total;
   private int L;
   private int price;
   private int temp_a;
   private int w;

    public int getTemp_c() {
        return temp_c;
    }

    public void setTemp_c(int temp_c) {
        this.temp_c = temp_c;
    }

    public int getCash() {
        return cash;
    }

    public void setCash(int cash) {
        this.cash = cash;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public int getL() {
        return L;
    }

    public void setL(int l) {
        L = l;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public int getTemp_a() {
        return temp_a;
    }

    public void setTemp_a(int temp_a) {
        this.temp_a = temp_a;
    }

    public int getW() {
        return w;
    }

    public void setW(int w) {
        this.w = w;
    }
}
